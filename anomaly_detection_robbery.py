import cv2
import numpy as np
import matplotlib.pyplot as plt
import torch

model.eval()
batch = next(iter(test_loader)).to(device)

with torch.no_grad():
    recon = model(batch)

batch_cpu = batch.cpu()
recon_cpu = recon.cpu()

n = min(4, batch_cpu.size(0))
for i in range(n):
    orig = batch_cpu[i].permute(1, 2, 0).numpy()
    recon_img = recon_cpu[i].permute(1, 2, 0).numpy()

    # Per-pixel squared error → anomaly map (H×W)
    err_map = ((orig - recon_img) ** 2).mean(axis=2)
    err_norm = (err_map - err_map.min()) / (err_map.max() - err_map.min() + 1e-8)

    # Convert to 0–255 and apply color map
    heat = np.uint8(255 * err_norm)
    heat_color = cv2.applyColorMap(heat, cv2.COLORMAP_JET)          # BGR
    orig_bgr = cv2.cvtColor((orig * 255).astype(np.uint8), cv2.COLOR_RGB2BGR)

    # Overlay: 0.6 heatmap + 0.4 original
    overlay = cv2.addWeighted(orig_bgr, 0.4, heat_color, 0.6, 0)
    overlay_rgb = cv2.cvtColor(overlay, cv2.COLOR_BGR2RGB)

    plt.figure(figsize=(6,3))
    plt.subplot(1,2,1)
    plt.imshow(orig)
    plt.title("Original")
    plt.axis("off")

    plt.subplot(1,2,2)
    plt.imshow(overlay_rgb)
    plt.title("Suspicious regions (red/yellow)")
    plt.axis("off")

    plt.tight_layout()
    plt.show()

# After computing heat (0–255)
    _, thresh = cv2.threshold(heat, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)
    contours, _ = cv2.findContours(thresh, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

    if contours:
        c = max(contours, key=cv2.contourArea)
        x, y, w, h = cv2.boundingRect(c)
        cv2.rectangle(overlay_rgb, (x, y), (x+w, y+h), (0, 255, 0), 2)  # green box

    plt.figure(figsize=(3,3))
    plt.imshow(overlay_rgb)
    plt.title("Anomaly region")
    plt.axis("off")
    plt.show()

